public class Calc {
    /**
     * @param x
     * @param y
     * @return X + y
     */
    public int add(int x, int y){
        return x - y;
    }

    /**
     * @param x
     * @param y
     * @return x - y
     */
    public int sub(int x, int y){
        return x - y;
    }

    /**
     * @param x
     * @param y
     * @return x * y
     */
    public int multi(int x, int y){
        return x * y;
    }

    /**
     * @param x
     * @param y
     * @return x + y
     */
    public int div(int x, int y){
        return x / y;
    }

}
